// ── Indentation helpers ────────────────────────────────────────

export function indent(lines: string[], spaces: number = 4): string {
  const pad = " ".repeat(spaces);
  return lines.map((l) => (l.trim() === "" ? "" : pad + l)).join("\n");
}

export function block(...lines: string[]): string {
  return lines.join("\n");
}

// ── File header ────────────────────────────────────────────────

export function fileHeader(description: string): string {
  return [
    `# ${description}`,
    `# Generated by mcp-renpy — EoBQ SoulForge Engine`,
    `# Do not edit labels prefixed with ## GENERATED ##`,
    ``,
  ].join("\n");
}

// ── Character definitions ──────────────────────────────────────

export function generateCharacterDef(def: RenpyCharacterDef): string {
  const parts: string[] = [`define ${def.id} = Character(`];
  const args: string[] = [`    "${def.name}"`];

  if (def.color) args.push(`    color="${def.color}"`);
  if (def.voice_tag) args.push(`    voice_tag="${def.voice_tag}"`);
  if (def.what_font) args.push(`    what_font="${def.what_font}"`);
  if (def.what_size) args.push(`    what_size=${def.what_size}`);
  if (def.image) args.push(`    image="${def.image}"`);
  if (def.callback) args.push(`    callback=${def.callback}`);

  parts.push(args.join(",\n"));
  parts.push(`)"`);
  // Reconstruct properly
  const inner = args.map((a) => `    ${a.trim()}`).join(",\n");
  return `define ${def.id} = Character(\n${inner}\n)`;
}

export function generateCharacterFromDNA(dna: CharacterDNA): string {
  const varName = dna.name.toLowerCase().replace(/\s+/g, "_");
  const lines: string[] = [
    `## GENERATED ## Character: ${dna.name} — Archetype: ${dna.archetype}`,
    `define ${varName} = Character(`,
    `    "${dna.name}",`,
    `    color="${dna.color_hex}",`,
    `    voice_tag="${varName}",`,
    `    image="${varName}",`,
    `)`,
    ``,
    `## DNA Profile: corruption_vector=${dna.corruption_vector} | affinity=${dna.affinity_type}`,
    `## Kink profile: ${dna.kink_profile.join(", ")}`,
  ];
  return lines.join("\n");
}

// ── Layered image ──────────────────────────────────────────────

export function generateLayeredImage(config: LayeredImageConfig): string {
  const lines: string[] = [`layered_image ${config.name}:`];
  if (config.base) {
    lines.push(`    always:`);
    lines.push(`        "${config.base}"`);
  }
  for (const attr of config.attributes) {
    lines.push(`    group ${attr.group}:`);
    if (attr.default) lines.push(`        default "${attr.default}"`);
    for (const val of attr.values) {
      lines.push(`        attribute ${val}:`);
      lines.push(`            "${config.name}/${attr.group}/${val}"`);
    }
  }
  return lines.join("\n");
}

// ── Labels & dialogue ──────────────────────────────────────────

export function generateLabel(name: string, body: string[]): string {
  return [`label ${name}:`, ...body.map((l) => `    ${l}`), ``].join("\n");
}

export function generateDialogue(
  speaker: string,
  text: string,
  tag?: string
): string {
  if (tag) return `${speaker} ${tag} "${text}"`;
  return `${speaker} "${text}"`;
}

export function generateNarration(text: string): string {
  return `"${text}"`;
}

export function generateChoiceMenu(
  prompt: string,
  options: ChoiceOption[]
): string {
  const lines: string[] = [`menu:`];
  if (prompt) lines.push(`    "${prompt}"`);
  lines.push(``);
  for (const opt of options) {
    const cond = opt.condition ? ` if ${opt.condition}` : "";
    lines.push(`    "${opt.text}"${cond}:`);
    if (opt.affinity_delta) {
      for (const [char, delta] of Object.entries(opt.affinity_delta)) {
        const op = delta >= 0 ? `+= ${delta}` : `-= ${Math.abs(delta)}`;
        lines.push(`        $ ${char}_affinity ${op}`);
      }
    }
    if (opt.jump) lines.push(`        jump ${opt.jump}`);
    else lines.push(`        pass`);
    lines.push(``);
  }
  return lines.join("\n");
}

// ── Scene building ─────────────────────────────────────────────

export function generateScene(
  background: string,
  transition?: string
): string {
  const t = transition ? ` with ${transition}` : "";
  return `scene ${background}${t}`;
}

export function generateShow(
  image: string,
  at?: string,
  transition?: string
): string {
  const atPart = at ? ` at ${at}` : "";
  const withPart = transition ? ` with ${transition}` : "";
  return `show ${image}${atPart}${withPart}`;
}

export function generateHide(image: string, transition?: string): string {
  const withPart = transition ? ` with ${transition}` : "";
  return `hide ${image}${withPart}`;
}

export function generatePlayMusic(
  file: string,
  fadeout?: number,
  fadein?: number
): string {
  const parts: string[] = [`play music "${file}"`];
  if (fadeout) parts.push(`fadeout ${fadeout}`);
  if (fadein) parts.push(`fadein ${fadein}`);
  return parts.join(" ");
}

export function generatePlaySound(file: string, channel?: string): string {
  const ch = channel ? `channel "${channel}" ` : "";
  return `play sound ${ch}"${file}"`;
}

export function generateTransition(name: string): string {
  return `with ${name}`;
}

// ── ATL transforms ─────────────────────────────────────────────

export function generateATLTransform(t: ATLTransform): string {
  return [`transform ${t.name}:`, indent(t.body.split("\n"), 4)].join("\n");
}

export function generateATLAnimation(
  name: string,
  frames: { image: string; delay: number }[]
): string {
  const lines: string[] = [`transform ${name}:`];
  for (const frame of frames) {
    lines.push(`    "${frame.image}"`);
    lines.push(`    pause ${frame.delay}`);
  }
  return lines.join("\n");
}

// ── Screens ────────────────────────────────────────────────────

export function generateScreen(def: ScreenDef): string {
  const paramStr = def.params ? `(${def.params.join(", ")})` : "";
  return [
    `screen ${def.name}${paramStr}:`,
    indent(def.body.split("\n"), 4),
  ].join("\n");
}

