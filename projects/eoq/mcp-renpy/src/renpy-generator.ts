export function generateDefault(name: string, value: string): string {
  return `default ${name} = ${value}`;
}

export function generateDefine(name: string, value: string): string {
  return `define ${name} = ${value}`;
}

export function generateConditionBlock(
  conditions: { condition: string; body: string[] }[],
  else_body?: string[]
): string {
  const lines: string[] = [];
  conditions.forEach((c, i) => {
    const keyword = i === 0 ? "if" : "elif";
    lines.push(`${keyword} ${c.condition}:`);
    lines.push(...c.body.map((l) => `    ${l}`));
  });
  if (else_body) {
    lines.push(`else:`);
    lines.push(...else_body.map((l) => `    ${l}`));
  }
  return lines.join("\n");
}

export function generatePythonBlock(code: string): string {
  return [`python:`, indent(code.split("\n"), 4)].join("\n");
}

