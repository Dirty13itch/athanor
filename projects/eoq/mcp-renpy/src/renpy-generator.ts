// ── H-scene generation ─────────────────────────────────────────

const INTENSITY_TAGS: Record<IntensityLevel, string> = {
  soft: "## [SOFT]",
  medium: "## [MEDIUM]",
  explicit: "## [EXPLICIT]",
  extreme: "## [EXTREME]",
};

export function generateHScene(cfg: HSceneConfig): string {
  const charVar = cfg.character_id;
  const tag = INTENSITY_TAGS[cfg.intensity];
  const lines: string[] = [
    `${tag} H-Scene: ${cfg.character_id} | Stage ${cfg.corruption_stage}`,
    `## DNA: ${cfg.dna.archetype} | vector: ${cfg.dna.corruption_vector}`,
    ``,
    `label ${cfg.label}:`,
  ];

  if (cfg.unlock_condition) {
    lines.push(`    ## Unlock condition: ${cfg.unlock_condition}`);
  }

  for (const act of cfg.acts) {
    lines.push(`    ## Act: ${act.id} — ${act.description}`);
    if (act.has_cg && cfg.cg_prefix) {
      lines.push(`    show ${cfg.cg_prefix}_${act.id} with dissolve`);
    }
    for (let i = 0; i < act.dialogue_count; i++) {
      lines.push(
        `    ${charVar} "## DIALOGUE ${i + 1}: [${cfg.dna.voice_style}] placeholder"`
      );
    }
    lines.push(``);
  }

  lines.push(`    return`);
  return lines.join("\n");
}

// ── Corruption arc ─────────────────────────────────────────────

export function generateCorruptionArc(
  queen_id: string,
  stages: CorruptionStage[]
): string {
  const lines: string[] = [
    `## Corruption arc: ${queen_id}`,
    `default ${queen_id}_corruption = 0`,
    ``,
  ];

  for (const stage of stages) {
    lines.push(`## Stage ${stage.stage}: ${stage.label}`);
    lines.push(`label ${queen_id}_corruption_${stage.stage}:`);
    lines.push(
      `    ## Trigger: ${stage.trigger_condition} | Type: ${stage.scene_type}`
    );
    lines.push(`    ## ${stage.description}`);
    lines.push(
      `    ## Intensity: ${stage.intensity} — fill with generated content`
    );
    lines.push(`    $ ${queen_id}_corruption = ${stage.stage}`);
    lines.push(`    return`);
    lines.push(``);
  }

  return lines.join("\n");
}

// ── Endings ────────────────────────────────────────────────────

export function generateEnding(cfg: EndingConfig): string {
  const lines: string[] = [
    `## Ending: ${cfg.type.toUpperCase()} — "${cfg.title}"`,
    `label ${cfg.label}:`,
    `    ## Conditions: ${cfg.conditions.join(" AND ")}`,
    `    ## ${cfg.description}`,
  ];

  if (cfg.music) lines.push(`    play music "${cfg.music}" fadein 2.0`);

  lines.push(`    scene black with dissolve`);

  if (cfg.cg) {
    lines.push(`    pause 1.0`);
    lines.push(`    show ${cfg.cg} with dissolve`);
  }

  lines.push(`    "## ENDING NARRATION PLACEHOLDER"`);
  lines.push(``);
  lines.push(`    ## Unlock achievement`);
  lines.push(`    $ renpy.achievement.grant("ending_${cfg.type}")`);
  lines.push(`    jump _return`);

  return lines.join("\n");
}

// ── Phone messages ─────────────────────────────────────────────

export function generatePhoneMessages(msgs: PhoneMessage[]): string {
  const lines: string[] = [`## Phone message sequence`];

  for (const msg of msgs) {
    lines.push(`label phone_${msg.sender}_${msg.timestamp_label}:`);
    lines.push(`    ## Obsession level: ${msg.obsession_level}/5`);
    lines.push(`    call screen phone_overlay`);
    for (const text of msg.messages) {
      lines.push(`    ## Message: "${text}"`);
    }
    if (msg.attachment) {
      lines.push(`    ## Attachment: ${msg.attachment}`);
    }
    lines.push(`    return`);
    lines.push(``);
  }

  return lines.join("\n");
}

