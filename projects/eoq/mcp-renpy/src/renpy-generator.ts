// ── Flowchart extraction ───────────────────────────────────────

export interface FlowNode {
  label: string;
  jumps: string[];
  calls: string[];
  menus: { prompt: string; options: string[] }[];
}

export function parseFlowchart(script: string): FlowNode[] {
  const nodes: FlowNode[] = [];
  const labelRegex = /^label\s+(\w+)\s*:/gm;
  const jumpRegex = /^\s+jump\s+(\w+)/gm;
  const callRegex = /^\s+call\s+(\w+)/gm;
  const menuRegex = /^\s+menu\s*:/gm;

  let labelMatch: RegExpExecArray | null;
  const labels: { name: string; start: number }[] = [];

  while ((labelMatch = labelRegex.exec(script)) !== null) {
    labels.push({ name: labelMatch[1], start: labelMatch.index });
  }

  for (let i = 0; i < labels.length; i++) {
    const label = labels[i];
    const end = i + 1 < labels.length ? labels[i + 1].start : script.length;
    const segment = script.slice(label.start, end);

    const jumps: string[] = [];
    let jm: RegExpExecArray | null;
    const jr = /jump\s+(\w+)/g;
    while ((jm = jr.exec(segment)) !== null) jumps.push(jm[1]);

    const calls: string[] = [];
    let cm: RegExpExecArray | null;
    const cr = /call\s+(\w+)/g;
    while ((cm = cr.exec(segment)) !== null) calls.push(cm[1]);

    nodes.push({ label: label.name, jumps, calls, menus: [] });
  }

  return nodes;
}

export function renderFlowchartDot(nodes: FlowNode[]): string {
  const lines: string[] = [
    `digraph EoBQ {`,
    `    rankdir=TB;`,
    `    node [shape=box, fontname="Helvetica"];`,
    ``,
  ];

  for (const node of nodes) {
    lines.push(`    "${node.label}" [label="${node.label}"];`);
    for (const j of node.jumps) {
      lines.push(`    "${node.label}" -> "${j}" [style=solid];`);
    }
    for (const c of node.calls) {
      lines.push(`    "${node.label}" -> "${c}" [style=dashed];`);
    }
  }

  lines.push(`}`);
  return lines.join("\n");
}

// ── Utility: extract strings ───────────────────────────────────

export function extractTranslatableStrings(script: string): string[] {
  const strings: string[] = [];
  // Match quoted strings not preceded by # or define/default
  const regex = /(?<!#.*)"([^"\\]|\\.)+"/g;
  let m: RegExpExecArray | null;
  while ((m = regex.exec(script)) !== null) {
    strings.push(m[0]);
  }
  return [...new Set(strings)];
}

// ── Utility: merge scripts ─────────────────────────────────────

export function mergeScripts(a: string, b: string): string {
  return [a.trimEnd(), ``, `## --- Merged from second file ---`, ``, b.trimStart()].join("\n");
}

// ── Utility: validate basic syntax ────────────────────────────

export interface ValidationResult {
  valid: boolean;
  errors: string[];
  warnings: string[];
}

export function validateScript(script: string): ValidationResult {
  const errors: string[] = [];
  const warnings: string[] = [];

  const lines = script.split("\n");

  // Check for unclosed quotes
  lines.forEach((line, i) => {
    const ln = i + 1;
    const stripped = line.replace(/##.*/g, "");
    const quotes = (stripped.match(/"/g) || []).length;
    if (quotes % 2 !== 0) {
      errors.push(`Line ${ln}: Unclosed string literal`);
    }
  });

  // Check label naming
  const labelRegex = /^label\s+([^\s:]+)\s*:/gm;
  const seen = new Set<string>();
  let m: RegExpExecArray | null;
  while ((m = labelRegex.exec(script)) !== null) {
    if (seen.has(m[1])) {
      errors.push(`Duplicate label: ${m[1]}`);
    }
    seen.add(m[1]);
  }

  // Check jumps resolve
  const jumpRegex = /jump\s+(\w+)/g;
  while ((m = jumpRegex.exec(script)) !== null) {
    if (!seen.has(m[1]) && m[1] !== "_return") {
      warnings.push(`Jump to undefined label: ${m[1]}`);
    }
  }

  return { valid: errors.length === 0, errors, warnings };
}
